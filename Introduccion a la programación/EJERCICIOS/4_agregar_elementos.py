'''
Agregar elementos
.append(): Agregar un elemento al final de la lista
'''
frutas = ["manzana", "fresa", "uva"]
frutas.append(["pera", "mango"])
print(frutas)

'''
.insert(): Inserta un elemento en el indice indicado, sin reemplazar valores
'''
frutas = ["manzana", "fresa", "uva"]
frutas.insert(1, "melon")
print(frutas)

